    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int flag = tblProducts.getSelectedRow();
        Car car = fleet.searchCar(flag+1);
        if(car != null){
            ViewProductDetailJPanel vpdjp = new ViewProductDetailJPanel(workArea,car);
            workArea.add("MainJFrame", vpdjp);
            CardLayout layout = (CardLayout) workArea.getLayout();
            layout.next(workArea);
        } else {
            JOptionPane.showMessageDialog(this, "Please choose a car", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnViewActionPerformed

