    public void refreshTable() {
        ArrayList<Car> carCatalog;
        DefaultTableModel model = (DefaultTableModel) tblProducts.getModel();
        model.setRowCount(0);
        carCatalog = this.fleet.getCarCatalog();
        for (int i=0; i<carCatalog.size();i++ ) {
            Object row[] = new Object[9];
            Car showCar = carCatalog.get(i);
            row[0] = showCar.getSerial();
            row[1] = showCar.getCertificate();
            row[2] = showCar.getManufacture();
            row[3] = showCar.getLocation();
            row[4] = showCar.getCity();
            row[5] = showCar.isAvailable();
            row[6] = showCar.getYear();
            row[7] = showCar.getSeats();
            Date d = showCar.getExpiry();
            String s = null;
            s = DateFormat.getDateInstance().format(d);
            row[8] = s;
            model.addRow(row);
        }
    }

